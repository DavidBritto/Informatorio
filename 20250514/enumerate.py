tareas = ["ordenar codigo", "hacer pruebas", "documentar", "subir a produccion"]
# #El bucle for itera sobre cada tarea de la lista
for idtarea, tarea in enumerate(tareas, start=1):
    #El bucle for itera sobre cada tarea de la lista
    print(f"La tarea {idtarea} es {tarea}")
# #El bucle for itera sobre cada tarea de la lista y la imprime en la consola 
